#include "3-calc.h"
/**
 * op_add - add two integers
 * @a: int
 * @b: int
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract b from a
 * @a: int
 * @b: int
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply 2 integers
 * @a: int
 * @b: int
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide a with b
 * @a: int
 * @b: int
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - perform mod
 * @a: int
 * @b: int
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
